    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(txtnumero.getText().length() ==0){
            JOptionPane.showConfirmDialog(rootPane,"Debes ingresar un Número de Habitación");
            txtnumero.requestFocus();
            return;
        }
        if(txtdescripcion.getText().length() ==0){
            JOptionPane.showConfirmDialog(rootPane,"Debes ingresar una descripción para la habitación");
            txtdescripcion.requestFocus();
            return;
        }
        
        if(txtPrecioDiario.getText().length() ==0){
            JOptionPane.showConfirmDialog(rootPane,"Debes ingresar una precio para la habitación");
            txtPrecioDiario.requestFocus();
            return;
        }
        
        habitacion hab=new habitacion();
        fhabitacion func=new fhabitacion();
        
        hab.setNumero(txtnumero.getText());
        
        int seleccionado=jcbpiso.getSelectedIndex();
        hab.setPiso((String)jcbpiso.getItemAt(seleccionado));
        
        hab.setDescripcion(txtdescripcion.getText());
        hab.setPreciodiario(Double.parseDouble(txtPrecioDiario.getText()));
        
        seleccionado=jcbestado.getSelectedIndex();
        hab.setEstado((String) jcbestado.getItemAt(seleccionado));
        
        seleccionado=jcbtipohabitacion.getSelectedIndex();
        hab.setTipohabitacion((String) jcbtipohabitacion.getItemAt(seleccionado));
        
        if(accion.equals("guardar")){
            if(func.insertar(hab)){
                JOptionPane.showMessageDialog(rootPane,"La habitacion fue registrada correctamente");
                mostrar("");
                inhabilitar();
      
            }
            else{
                JOptionPane.showMessageDialog(rootPane,"La habitacion no fue registrada correctamente");
                
            }
        }
        else if(accion.equals("editar")){
            hab.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
            
            if(func.editar(hab)){
                JOptionPane.showMessageDialog(rootPane,"La habitacion fue editada correctamente");   
                mostrar("");
                inhabilitar();
            }          
        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        if (!txtidhabitacion.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estas seguro de eliminar la habitacion?","confirmar",2);
            
            if (confirmacion==0) {
                fhabitacion func=new fhabitacion();
                habitacion hab= new habitacion();
                
                hab.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
                func.eliminar(hab);
                mostrar("");
                inhabilitar();
                
            }
            
        }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

