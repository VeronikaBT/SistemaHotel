        accion="editar";
        
        int fila = jbtlHabitaciones.rowAtPoint(evt.getPoint());
        
        txtidhabitacion.setText(jbtlHabitaciones.getValueAt(fila,0).toString());
        txtnumero.setText(jbtlHabitaciones.getValueAt(fila,1).toString());
