    private void bntIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntIngresarActionPerformed
        // TODO add your handling code here:
        try {
            DefaultTableModel modelo;
            fusuario func=new fusuario();
            usuario usr=new usuario();
            
            usr.setUser(txtUsuario.getText());
            usr.setContra(txtcontra.getText());
            
            modelo=func.login(usr.getUser(), usr.getContra());
            
            jtblLogin.setModel(modelo);
            
            if (func.totalregistros>0) {
                this.dispose();
                frminicio form=new frminicio();
                form.toFront();
                form.setVisible(true);  
                //frminicio.lblidpersona.setText(jtblLogin.getValueAt(0, 0).toString());
                frminicio.lblnombre.setText(jtblLogin.getValueAt(0, 1).toString());
                frminicio.lblapaterno.setText(jtblLogin.getValueAt(0, 2).toString()+"!");
                //frminicio.lblamaterno.setText(jtblLogin.getValueAt(0, 3).toString());
                frminicio.lblCargo.setText(jtblLogin.getValueAt(0, 4).toString());
                
                if (!frminicio.lblCargo.getText().equals("Administrador")) {
                    frminicio.mnuArchivo.setEnabled(false);
                    frminicio.mnuConfiguraciones.setEnabled(false);
                } 
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Acceso Denegado","Acceso al Sistema",JOptionPane.ERROR_MESSAGE);                    
                }
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_bntIngresarActionPerformed

